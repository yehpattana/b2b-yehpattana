import { Bounce, toast } from "react-toastify";

export function toastSuccess(text:string){
    return  toast.success(text, {
        position: "top-right",
        autoClose: 1000,
        hideProgressBar: true,
        closeOnClick: true,
        pauseOnHover: true,
        draggable: true,
        progress: undefined,
        theme: "light",
        transition: Bounce,
      });
}

